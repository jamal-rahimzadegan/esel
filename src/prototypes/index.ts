// Array ////////////////////////////////////////////////////////////////
Array.prototype.groupBy = function (key: string) {
  return this.reduce((group, element) => {
    if (element[key] === undefined) return group;
    return Object.assign(group, { [element[key]]: (group[element[key]] || []).concat(element) });
  }, {});
};

